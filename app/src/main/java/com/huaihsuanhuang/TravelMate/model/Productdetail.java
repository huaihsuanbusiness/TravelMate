package com.huaihsuanhuang.TravelMate.model;

public class Productdetail {

    private String aname;
    private String bimage;
    private String cdescrip;
    private String dprice;
    private String ediscount;
    private String fmenuId;
    private String id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAname() {
        return aname;
    }

    public void setAname(String aname) {
        this.aname = aname;
    }

    public String getBimage() {
        return bimage;
    }

    public void setBimage(String bimage) {
        this.bimage = bimage;
    }

    public String getCdescrip() {
        return cdescrip;
    }

    public void setCdescrip(String cdescrip) {
        this.cdescrip = cdescrip;
    }

    public String getDprice() {
        return dprice;
    }

    public void setDprice(String dprice) {
        this.dprice = dprice;
    }

    public String getEdiscount() {
        return ediscount;
    }

    public void setEdiscount(String ediscount) {
        this.ediscount = ediscount;
    }

    public String getFmenuId() {
        return fmenuId;
    }

    public void setFmenuId(String fmenuId) {
        this.fmenuId = fmenuId;
    }
}
